"""
28 - Faça a leitura de três valores e apresente como resultado a soma dos
quadrados dos três valores lidos.
"""

valor1 = int(input("Digite o valor do primeiro número: "))
valor2 = int(input("Digite o valor do segundo número: "))
valor3 = int(input("Digite o valor do terceiro número: "))

soma = (valor1 ** 2) + (valor2 **2) + (valor3 ** 2)
quadrado = soma 

print(f"A soma do quadrado dos três valores são: {quadrado}.")
